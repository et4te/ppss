-module(cl_example_app).

-behaviour(application).

%%------------------------------------------------------------------------------
%% External Exports
%%------------------------------------------------------------------------------
-export([start/2, shutdown/0, stop/1]).

%%==============================================================================
%% External Functions
%%==============================================================================

start(Type, Args) ->
  case cl_example_sup:start_link(Args) of
    {ok, Pid} ->
      {ok, Pid};
    Error ->
      Error
  end.

shutdown() ->
  application:stop(cl_example_app).

%%==============================================================================
%% Internal Functions
%%==============================================================================

stop(State) ->
  ok.
